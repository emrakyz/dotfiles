[ -z "${1}" ] && printf "%s\n" "Specify a video file to re-encode." && exit "1"

av1an -i "${1}" \
        -o "av1_opus_${1%.*}.mkv" \
	--encoder "svt-av1" \
	--split-method "av-scenechange" \
        --workers "1" \
        --chunk-method "lsmash" \
	--sc-method "standard" \
	--resume \
        --pix-format "yuv420p10le" \
        --video-params "--preset -1 \
	                --crf 15 \
			--tune 2 \
			--enable-qm 1 \
			--qm-min 0 \
			--qm-max 10 \
			--input-depth 10 \
			--lookahead 120 \
			--enable-overlays 1 \
			--scd 1 \
			--keyint 48 \
			--lp 0 \
			--pin 0 \
			--enable-dlf 1 \
			--fast-decode 0 \
			--irefresh-type 2 \
			--enable-restoration 1 \
			--scm 2 \
			--enable-tf 1 \
			--progress 0 \
			--aq-mode 2 \
			--enable-cdef 1 \
			--enable-tpl-la 1 \
			--color-range 0 \
			--film-grain 0 \
			--film-grain-denoise 0"
